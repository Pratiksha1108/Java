import java.util.*;
public class OddEven {
    public static void main(String args[])
    {
        Scanner s =new Scanner(System.in);
        System.out.println("n");
        int n=s.nextInt();

        if(n%2==0)
        {
            System.out.println("N is even");
    
        }
        else
        {
            System.out.println("N is odd");
        }
    }
    
}
